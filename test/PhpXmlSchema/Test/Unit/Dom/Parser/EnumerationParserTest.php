    }
    
    /**
     * Returns a set of valid "id" attributes.
     * 
     * @return  array[]
     */
    public function getValidIdAttributes(): array
    {
        return [
            'Starts with _' => [
                'enumeration_id_0001.xsd', '_foo', 
            ], 
            'Starts with letter' => [
                'enumeration_id_0002.xsd', 'f', 
            ], 
            'Contains letter' => [
                'enumeration_id_0003.xsd', 'foo', 
            ], 
            'Contains digit' => [
                'enumeration_id_0004.xsd', 'f00', 
            ], 
            'Contains .' => [
                'enumeration_id_0005.xsd', 'f.bar', 
            ], 
            'Contains -' => [
                'enumeration_id_0006.xsd', 'f-bar', 
            ], 
            'Contains _' => [
                'enumeration_id_0007.xsd', 'f_bar', 
            ], 
            'Surrounded by whitespaces' => [
                'enumeration_id_0008.xsd', 'foo_bar', 
            ], 
        ];
    }
