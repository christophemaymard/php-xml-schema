            'Starts with _' => [
                'simpletype_id_0001.xsd', '_foo', 
            ], 
            'Starts with letter' => [
                'simpletype_id_0002.xsd', 'f', 
            ], 
            'Contains letter' => [
                'simpletype_id_0003.xsd', 'foo', 
